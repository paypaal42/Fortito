int		ft_sqrt(int nb)
{
	int i;
	int nb1;

	i = 0;
	nb1 = 0;
	if (nb < 0)
		return (0);
	while (nb1 < nb)
	{
		i++;
		nb1 = i * i;
	}
	return (nb1 == nb) ? (i) : (0);
}
