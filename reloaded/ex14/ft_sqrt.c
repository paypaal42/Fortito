int		ft_sqrt(int nb)
{
	int i;
	int result;

	i = 0;
	result = 0;
	if (nb < 0)
		return (0);
	while (result < nb)
	{
		result = i * i;
		i++;
	}
	return (result == nb) ? (i - 1) : (0);
}
