#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*dst;

	i = ft_strlen((char *)str);
	if (!(dst = malloc(sizeof(char) * i + 1)))
		return (NULL);
	i = 0;
	while (str[i])
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
