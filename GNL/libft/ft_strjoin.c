#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*new;
	size_t			i;

	s1 = (s1 == NULL) ? "" : s1;
	s2 = (s2 == NULL) ? "" : s2;
	if (s1 && s2)
	{
		i = ft_strlen((char *)s1) + ft_strlen((char *)s2);
		new = ft_strnew(i);
		if (new)
		{
			new = ft_strcpy(new, s1);
			new = ft_strcat(new, s2);
			return (new);
		}
	}
	return (NULL);
}
