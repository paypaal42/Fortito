#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*new_s;

	i = 0;
	if (s == 0)
		return (NULL);
	while (s[i])
		i++;
	if (!(new_s = (char*)malloc(sizeof(*new_s) * (i + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_s[i] = f(i, s[i]);
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
