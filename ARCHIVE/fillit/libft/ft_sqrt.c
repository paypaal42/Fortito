#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int nb1;

	i = 0;
	nb1 = 0;
	if (nb < 0)
		return (0);
	while (nb1 < nb)
	{
		i++;
		nb1 = i * i;
	}
	if (nb1 == nb)
		return (i);
	return (ft_sqrt(nb1));
}
