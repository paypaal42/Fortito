char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;
	char tab[];

	i = 0;
	j = 0;
	while (src[i] != '\0')
	{
		tab[i] = src[i];
		i++;
	}
	while (dest[j] != '\0')
	{
		tab[i + j] = dest[j];
		j++;
	}
	return (tab);
}
