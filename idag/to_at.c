int	ft_atoi(char *str)
{
	int	i;
	int flag;
	int nb;

	i = 0;
	flag = 0;
	nb = 0;
	while (str[i] < 33)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			flag = 1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (flag ? -nb : nb);
}
